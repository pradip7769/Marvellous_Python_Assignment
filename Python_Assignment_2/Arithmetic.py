
def Add(value1, value2):
    sum = value1 + value2
    print("Addition is : ", sum)

def Sub(value1, value2):
    sub = value1 - value2
    print("Addition is : ", sub)

def Mult(value1, value2):
    mul = value1 * value2
    print("Addition is : ", mul)

def Div(value1, value2):
    div = value1 / value2
    print("Addition is : ", div)
